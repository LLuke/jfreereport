package org.jfree.report.modules.factories.common;

import org.xml.sax.Attributes;
import org.xml.sax.SAXException;

/**
 * Creation-Date: 07.04.2006, 17:50:34
 *
 * @author Thomas Morgner
 */
public class PropertyReadHandler extends StringReadHandler
{
  private String name;

  public PropertyReadHandler()
  {
  }

  /**
   * Starts parsing.
   *
   * @param attrs the attributes.
   * @throws SAXException if there is a parsing error.
   */
  protected void startParsing(final Attributes attrs) throws SAXException
  {
    super.startParsing(attrs);
    name = attrs.getValue(getUri(), "name");
    if (name == null)
    {
      throw new NullPointerException("Required attribute 'name' missing");
    }
  }

  public String getName()
  {
    return name;
  }
}
